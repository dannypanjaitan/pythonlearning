#meminta input 
bil1 = input('Masukkan bilangan pertama')
bil2 = input('Masukkan bilangan kedua')

#menjumlahkan 
jumlah = float(bil1) + float(bil2)

#mengurangkan
kurang = float(bil1) - float(bil2)

#mengalikan 
kali = float(bil1) * float(bil2)

#membagi
bagi = float(bil1) / float(bil2)

#output semua
print('Jumlah dari {0} + {1} adalah {2}'.format(bil1,bil2,jumlah))
print('Pengurangan dari {0} + {1} adalah {2}'.format(bil1,bil2,kurang))
print('Perkalian dari {0} + {1} adalah {2}'.format(bil1,bil2,kali))
print('Pembagian dari {0} + {1} adalah {2}'.format(bil1,bil2,bagi))